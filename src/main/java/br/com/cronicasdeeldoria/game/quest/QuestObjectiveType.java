package br.com.cronicasdeeldoria.game.quest;

/**
 * Tipos de objetivos que uma quest pode ter.
 */
public enum QuestObjectiveType {
    COLLECT_ITEM,      // Coletar item específico
    KILL_NPC,          // Derrotar NPC específico
    TALK_TO_NPC,       // Falar com NPC específico
    REACH_LOCATION,    // Chegar a localização
    DEPOSIT_ITEM,      // Depositar item em local específico
    DELIVER_ITEM       // Entregar item para NPC
}

